												<h2>MyTrust Multi-factor Authentication (MFA)</h2>

												<p>Advanced cyberthreats targeting government and financial institutions are growing in frequency and sophistication. With a long-standing focus on secure authentication mechanism, MSC Trustgate helps government and financial institutions implement secure MFA for transactions across various applications and multiple access channels.</p>

												<p>MFA is an authentication platform that supports the use of more than one verification methods. It enhances the security of identity verification for online transactions. MFA delivers a secure, scalable, reliable and centralized authentication and management platform to provide identity theft protection and protection from phishing attacks. MFA authentication scheme typically must include two of the three schemes: something the user knows (e.g. PIN), something the user has (e.g. Token), something the user is (e.g. Digital Identity).</p>

												<p>Internet applications can leverage on MSC Trustgate’s MFA platform to verify the credentials of users via multiple form factors such as User-Password, SMS-OTP (One Time Password), FIDO (Fast ID On-Line), Mobile OTP, OCRA Token and QR code. MSC Trustgate’s MFA can effectively mitigate risk for online access or transaction to enable true efficiency and satisfying customers’ complex requirement.</p>

												<h2>Adaptive Intelligent System</h2>
